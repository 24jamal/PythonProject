# 1. Python Objects

        # In Python, everything is an object (numbers, strings, functions, even classes).

        # Objects are created from classes (a blueprint).

        # Each object has:

            # Identity → unique ID (like memory address) → id(obj)

            # Type → defines what kind of object → type(obj)

            # Value → data stored inside the object


x = 10

print(x)
print(type(x))
print(id(x))
